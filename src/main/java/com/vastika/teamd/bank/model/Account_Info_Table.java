package com.vastika.teamd.bank.model;

public class Account_Info_Table {
	private int id;
	private String account_name;
	private String address;
	private long mobile_number;
	private long unique_id_type;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getAccount_name() {
		return account_name;
	}
	public void setAccount_name(String account_name) {
		this.account_name = account_name;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public long getMobile_number() {
		return mobile_number;
	}
	public void setMobile_number(long mobile_number) {
		this.mobile_number = mobile_number;
	}
	public long getUnique_id_type() {
		return unique_id_type;
	}
	public void setUnique_id_type(long unique_id_type) {
		this.unique_id_type = unique_id_type;
	}
	
	

}
