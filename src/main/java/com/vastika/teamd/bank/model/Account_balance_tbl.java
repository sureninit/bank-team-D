public class Account_balance_tbl {
	private String deposit_amount;
	private String withdraw_amount;
	private String balance_account_info_id;
	public String getDeposit_amount() {
		return deposit_amount;
	}
	public void setDeposit_amount(String deposit_amount) {
		this.deposit_amount = deposit_amount;
	}
	public String getWithdraw_amount() {
		return withdraw_amount;
	}
	public void setWithdraw_amount(String withdraw_amount) {
		this.withdraw_amount = withdraw_amount;
	}
	public String getBalance_account_info_id() {
		return balance_account_info_id;
	}
	public void setBalance_account_info_id(String balance_account_info_id) {
		this.balance_account_info_id = balance_account_info_id;
	}
	

}
